package exercise2.examples.sottopackage.sottosottopackage;

public class Pair {

    private int x;
    private int y;

    public Pair(final int x, final int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
